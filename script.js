const navToggle = document.querySelector(".nav-toggle");
const nav = document.querySelector(".nav");

if (navToggle && nav) {
  navToggle.addEventListener("click", () => {
    const expanded = navToggle.getAttribute("aria-expanded") === "true";
    navToggle.setAttribute("aria-expanded", String(!expanded));
    nav.classList.toggle("is-open", !expanded);
    document.body.classList.toggle("nav-open", !expanded);
  });

  nav.querySelectorAll("a").forEach((link) => {
    link.addEventListener("click", () => {
      navToggle.setAttribute("aria-expanded", "false");
      nav.classList.remove("is-open");
      document.body.classList.remove("nav-open");
    });
  });
}

const revealItems = document.querySelectorAll(
  ".hero__copy, .hero__visual, .trust-strip, .section, .visit-card, .footer"
);

const observer = new IntersectionObserver(
  (entries) => {
    entries.forEach((entry) => {
      if (entry.isIntersecting) {
        entry.target.classList.add("is-visible");
        observer.unobserve(entry.target);
      }
    });
  },
  { threshold: 0.14 }
);

revealItems.forEach((item) => {
  item.classList.add("reveal");
  observer.observe(item);
});

function updateScrollProgress() {
  const scrollTop = window.scrollY;
  const docHeight = document.documentElement.scrollHeight - window.innerHeight;
  const progress = docHeight > 0 ? (scrollTop / docHeight) * 100 : 0;
  document.documentElement.style.setProperty("--scroll-progress", `${progress}%`);
  document.documentElement.style.setProperty("--hero-shift", `${Math.min(scrollTop * 0.08, 18)}`);
}

window.addEventListener("scroll", updateScrollProgress, { passive: true });
updateScrollProgress();

const langButtons = document.querySelectorAll("[data-lang-trigger]");
const i18nNodes = document.querySelectorAll("[data-i18n]");

function setLanguage(lang) {
  document.documentElement.lang = lang;
  langButtons.forEach((button) => {
    button.classList.toggle("is-active", button.dataset.langTrigger === lang);
  });

  i18nNodes.forEach((node) => {
    const key = node.dataset.i18n;
    const translation = translations[lang]?.[key];
    if (!translation) {
      return;
    }

    if (node.tagName === "INPUT" || node.tagName === "TEXTAREA") {
      node.placeholder = translation;
      return;
    }

    if (node.tagName === "OPTION") {
      node.textContent = translation;
      return;
    }

    node.textContent = translation;
  });
}

langButtons.forEach((button) => {
  button.addEventListener("click", () => {
    setLanguage(button.dataset.langTrigger);
  });
});

const form = document.querySelector("#reservation-form");
const feedback = document.querySelector("#form-feedback");

if (form && feedback) {
  form.addEventListener("submit", (event) => {
    event.preventDefault();

    const formData = new FormData(form);
    const name = String(formData.get("name") || "").trim();
    const phone = String(formData.get("phone") || "").trim();
    const branch = String(formData.get("branch") || "").trim();
    const type = String(formData.get("type") || "").trim();
    const guests = String(formData.get("guests") || "").trim();
    const message = String(formData.get("message") || "").trim();

    if (!name || !phone) {
      feedback.classList.remove("is-success");
      feedback.textContent =
        document.documentElement.lang === "en"
          ? "Please enter at least your name and phone number."
          : "Доод тал нь нэр болон утсаа оруулна уу.";
      return;
    }

    feedback.classList.add("is-success");
    feedback.textContent =
      document.documentElement.lang === "en"
        ? `Inquiry ready for ${branch}. Reservation handoff: ${type} request from ${name} (${phone})${guests ? ` for ${guests} guests` : ""}${message ? `. Notes: ${message}` : "."}`
        : `${branch} салбарт зориулсан хүсэлт бэлэн боллоо. Reservation handoff: ${type} хүсэлт, ${name} (${phone})${guests ? `, ${guests} хүн` : ""}${message ? `. Нэмэлт: ${message}` : "."}`;
  });
}

const prefersReducedMotion = window.matchMedia("(prefers-reduced-motion: reduce)");

if (!prefersReducedMotion.matches) {
  const hero = document.querySelector(".hero");

  if (hero) {
    hero.addEventListener("pointermove", (event) => {
      const rect = hero.getBoundingClientRect();
      const x = (event.clientX - rect.left) / rect.width;
      const y = (event.clientY - rect.top) / rect.height;
      document.documentElement.style.setProperty("--pointer-x", x.toFixed(3));
      document.documentElement.style.setProperty("--pointer-y", y.toFixed(3));
    });
  }
}
